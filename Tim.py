#Peperoni Pizza $5, Meat lovers pizza $7, Pepsi $ 2.50.
peperoni_pizza = 5
meat_lovers_pizza = 7
pepsi = 2.50

#calculates cost for order 1. 2 peperoni, 5 meat lovers, 3 pepsi.
order_1 = 2 * peperoni_pizza + 5 * meat_lovers_pizza + 3 * pepsi

#calculates cost for order 2. 1 peperoni, 2 meatlovers, 4 pepsi.
order_2 = 1 * peperoni_pizza + 2 * meat_lovers_pizza + 4 * pepsi


#final cost.

print("The final cost for order 1 comes to: $ {}".format(order_1))

print("The final cost for order 2 comes to: $ {}".format(order_2))


